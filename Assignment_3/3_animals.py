class Tiger:  # Encapsulation

    def __init__(self):
        self.__legs = 4  # Private Variables (Abstraction)
        self.__eyes = 2
        self.__ears = 2
        self.__tail = 1
        self.__claws = 4

    def get_Properties(self):
        print("******************************")
        print("Tiger : ")
        print("Legs = ", self.__legs)
        print("Eyes = ", self.__eyes)
        print("Ears = ", self.__ears)
        print("Tail = ", self.__tail)
        print("Claws on each paw = ", self.__claws)


